package eu.reitmayer.schemagen.lang;

public class SchemaDomain {

	public String id;
	
	public String type;
	
	public Integer length;
	
	public Integer precision;
	

	
	public SchemaDomain(String id, String type, Integer length,
			Integer precision) {
		super();
		this.id = id;
		this.type = type;
		this.length = length;
		this.precision = precision;
	}



	public SchemaDomain(String id) {
		super();
		this.id = id;
	}



	public String getId() {
		return id;
	}



	public void setId(String id) {
		this.id = id;
	}



	public String getType() {
		return type;
	}



	public void setType(String type) {
		this.type = type;
	}



	public Integer getLength() {
		return length;
	}



	public void setLength(Integer length) {
		this.length = length;
	}



	public Integer getPrecision() {
		return precision;
	}



	public void setPrecision(Integer precision) {
		this.precision = precision;
	}



	@Override
	public String toString() {
		return "SchemaDomain [id=" + id + ", type=" + type + ", length="
				+ length + ", precision=" + precision + "]";
	}

	
	
}
